    private void setMaxUsedValue() {
        float usedValues;
        List<Float> list;

        list = SortUtils.instance().sortValues(info.getDistance());;

        int size = list.size();
        int index = (int) (size * (jSlider1.getValue() / 100f));

        if (index == 0) {
            usedValues = list.get(0);
        } else {
            usedValues = list.get(index - 1);
        }

        info.setMaxThreshValue(usedValues);

        updateHistograms();
        tresholdValueChanged = false;
    }

