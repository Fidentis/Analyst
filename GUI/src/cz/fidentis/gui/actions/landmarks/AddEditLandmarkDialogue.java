    private void setConfiguration(int id, String name, String desc){
        int defaultFpNumber = FacialPointType.values().length;
        
        if(id < 0){  //next possible ID
            id = defaultFpNumber;
        }else if(id <= defaultFpNumber){     //editing default landmarks, can't change ID
            landmarkIDspinner.setEnabled(false);
        }
        
        
        SpinnerNumberModel model = new SpinnerNumberModel(id, 0, Integer.MAX_VALUE,1.0);
        landmarkIDspinner.setModel(model);
        
        landmarkDescriptionField.setText(desc);
        landmarkNameField.setText(name);
    }

